void map_init(Map *map) {
    memcpy(map->mini_map, s_mini_map, sizeof(s_mini_map));
    map->rows = MAP_ROWS;
    map->cols = MAP_COLS;
}

int map_get(const Map *map, int x, int y) {
    if (x < 0 || x >= MAP_COLS || y < 0 || y >= MAP_ROWS)
        return 1; /* out of bounds treated as wall */
    return map->mini_map[y][x];
}

int map_is_wall(const Map *map, int x, int y) {
    return map_get(map, x, y) != 0;
}

void map_draw(const Map *map, SDL_Renderer *renderer) {
    SDL_SetRenderDrawColor(renderer, 64, 64, 64, 255);
    for (int y = 0; y < map->rows; y++) {
        for (int x = 0; x < map->cols; x++) {
            if (map->mini_map[y][x]) {
                SDL_Rect r = { x * 100, y * 100, 100, 100 };
                SDL_RenderDrawRect(renderer, &r);
            }
        }
    }
}
